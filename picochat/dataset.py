"""Where raw training data comes from (nanochat's dataset.py analogue):
Hugging Face Hub sources for both pretraining text and SFT chat
conversations, and how to stream them.

DatasetSpec/TEXT_PRESETS/Mixture describe pretraining text sources; both
tokenizer training (scripts/tok_train.py) and preprocessing
(scripts/base_setup.py) read from them. ChatDatasetSpec/CHAT_PRESETS describe
conversation sources for SFT (scripts/sft_setup.py). Tokenizing, packing and
loading the results live in picochat.dataloader.
"""

from dataclasses import dataclass, field, replace
from typing import Callable, Iterator

from datasets import load_dataset


@dataclass
class DatasetSpec:
    path: str  # HF Hub repo id, e.g. "roneneldan/TinyStories"
    name: str | None = None  # config / subset name, e.g. "20231101.en"
    split: str = "train"
    text_key: str = "text"  # column that holds the text
    # Use this to format several columns into one text (e.g. GSM8K question + answer).
    format: Callable[[dict], str] | None = field(default=None, repr=False)

    def to_text(self, row: dict) -> str:
        if self.format is not None:
            return self.format(row)
        return row[self.text_key]


